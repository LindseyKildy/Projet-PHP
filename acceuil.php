                <h2>Les Différents Types de Vin</h2>
                <p>Il existe plusieurs types de vin, chacun ayant des caractéristiques uniques. Les trois catégories principales sont :</p>
                <ul>
                    <li><strong>Vin Rouge :</strong> Le vin rouge est produit à partir de raisins noirs et possède des arômes puissants. Il est souvent servi avec des viandes rouges et des plats en sauce.</li>
                    <li><strong>Vin Blanc :</strong> Le vin blanc est fait à partir de raisins blancs ou noirs, mais sans les peaux. Il est généralement plus léger et acide, idéal avec des poissons ou des fruits de mer.</li>
                    <li><strong>Vin Rosé :</strong> Le vin rosé est un mélange de vin rouge et blanc, ou un vin rouge dont les peaux sont retirées rapidement. Il est frais et fruité, parfait pour les repas estivaux.</li>
                </ul>
